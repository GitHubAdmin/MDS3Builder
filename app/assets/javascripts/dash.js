$(document).ready(function(){
  $('.orba_assessments').hide();
  $("input[name=orba]").change(function() 
    {
      if(this.checked)
        $(".orba_assessments").fadeIn('slow');
      else
        $(".orba_assessments").fadeOut('slow');
    });
  $('.pps_assessments').hide();
    $("input[name=pps]").change(function() 
    {
      if(this.checked)
        $(".pps_assessments").fadeIn('slow');
      else
        $(".pps_assessments").fadeOut('slow');
    });
  $('.transfer_assessments').hide();
    $("input[name=transfers]").change(function() 
    {
      if(this.checked)
        $(".transfer_assessments").fadeIn('slow');
      else
        $(".transfer_assessments").fadeOut('slow');
    });
    
});
